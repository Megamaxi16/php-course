<?php

function soma($num1, $num2){
    $result = $num1 + $num2;
    return $result;
}

function multiplica($num1, $num2){
    $result = $num1*$num2;
    return $result;
}

function divide($num1, $num2){
    $result = $num1/$num2;
    return $result;
}

function subtrai($num1, $num2){
    $result = $num1 - $num2;
    return $result;
}

?>